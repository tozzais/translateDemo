public class StringKey {
    private String key;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    @Override
    public String toString() {
        return "StringKey{" +
                "key='" + key + '\'' +
                '}';
    }
}
